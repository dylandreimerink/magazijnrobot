public class Product {
	private int locationX;
	private int locationY;

	public Product(int locationX, int locationY) {
		this.locationX = locationX;
		this.locationY = locationY;
	}

	public int getLocationX() {
		return locationX;
	}

	public int getLocationY() {
		return locationY;
	}
}
